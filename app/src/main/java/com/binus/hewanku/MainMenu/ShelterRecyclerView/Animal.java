package com.binus.hewanku.MainMenu.ShelterRecyclerView;

public class Animal {
    private String name;
    private int photo;
    private int backgroundText;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public int getBackgroundText() {
        return backgroundText;
    }

    public void setBackgroundText(int backgroundText) {
        this.backgroundText = backgroundText;
    }
}
